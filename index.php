                <div class="upBox_p col-md-6">
                    <p class="descr">Важнейшее условие для получения качественных готовых изделий. Для продукции, которая готовится во фритюре, рекомендуется использовать муку, содержащую не менее 27 процентов клейковины высокой эластичности. Соблюдение этого условия поможет добиться необходимого для пончиков объема и пышности, кроме того, изделия будут дольше сохранять аппетитный внешний вид и форму. Если же Вы по каким-то причинам вынуждены использовать муку с невысокими показателями, то на помощь придут улучшители.
                    </p>
                </div>
            </div>
            <div class="team-item col-md-12 scroll-animate ">
                <div class="item col-lg-4 animation ">
                    <img src="img/team_1.png" alt=""/>
                    <h3>75% <br><span class="a1">мука</span></h3>
                    <p>Неотъемлемым ингредиентом теста для пончиков являются: мука. Тесто можно приготовить с добавлением кефира или молока. Для пышности в тесто добавляют немного гашеной уксусом соды, а для вкуса - несколько граммов соли.</p>
                </div>
                <div class="item col-lg-4 animation ">
                    <img src="img/team_2.png" alt=""/>
                    <h3>15% <br><span>молоко</span></h3>
                    <p>Это самый простой ингридиент, для пончиков. С молоком сами пончики получаются нежными, тоненькими, с небольшим количеством дырочек. Это самый лучший вариант, если пончик должен быть всего лишь оболочкой для вкусной, интересной начинки. Так-же можно приготовить пончик на кефире или простокваше.
                    </p>
                </div>
                <div class="item col-lg-4 animation ">
